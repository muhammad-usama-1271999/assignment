//question39: city names: formatting city-country pairs.



function city_country(city : string, country : string):string{
    return `${city} , ${country}`;
}

console.log(city_country("Hyderabad", "Pakistan"));
console.log(city_country("masco", "russia"));
console.log(city_country("anatolia", "turkey"));
console.log(city_country("washington","america"))